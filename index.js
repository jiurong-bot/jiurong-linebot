                await reply(event.replyToken, 'ÂØÜÁ¢ºÈåØË™§„ÄÇÂ∑≤Ëá™ÂãïÂàáÊèõÂõûÂ≠∏Âì°Ë∫´‰ªΩ„ÄÇ');
